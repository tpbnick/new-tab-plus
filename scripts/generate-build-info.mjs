import { execSync } from 'node:child_process';
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const pkg = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'));
const manifest = JSON.parse(readFileSync(join(root, 'manifest.json'), 'utf8'));
const repoUrl = 'https://github.com/tpbnick/new-tab-plus';
const tagline =
  typeof manifest.description === 'string' && manifest.description.trim()
    ? manifest.description.trim()
    : 'A bookmarks-driven, widget-extensible new tab page.';

const placeholder = {
  shortSha: '0000000',
  fullSha: '0'.repeat(40),
  date: '—',
};

let commit = { ...placeholder };

try {
  const shortSha = execSync('git rev-parse --short HEAD', { cwd: root, encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim();
  const fullSha = execSync('git rev-parse HEAD', { cwd: root, encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim();
  const date = execSync("git log -1 '--format=%cI'", { cwd: root, encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim().slice(0, 10);
  commit = { shortSha, fullSha, date: date || placeholder.date };
} catch {
  /* no git repo or no commits yet — keep placeholder */
}

const outPath = join(root, 'src', 'lib', 'about', 'appMeta.generated.ts');
const content = `// Generated by scripts/generate-build-info.mjs — do not edit.
export const APP_VERSION = ${JSON.stringify(pkg.version)};
export const APP_TAGLINE = ${JSON.stringify(tagline)};
export const GITHUB_REPO_URL = ${JSON.stringify(repoUrl)};
export const GITHUB_ISSUES_URL = ${JSON.stringify(`${repoUrl}/issues/new`)};
export const GITHUB_LICENSE_URL = ${JSON.stringify(`${repoUrl}/blob/main/LICENSE`)};
export const AUTHOR_NAME = 'Nick Platt';
export const AUTHOR_URL = 'https://nickplatt.dev';

export const LATEST_CHANGE = {
  shortSha: ${JSON.stringify(commit.shortSha)},
  date: ${JSON.stringify(commit.date)},
  commitUrl: ${JSON.stringify(`${repoUrl}/commit/${commit.fullSha}`)},
} as const;
`;

writeFileSync(outPath, content, 'utf8');
console.log(`Wrote ${outPath} (v${pkg.version}, ${commit.shortSha})`);
